#include<iostream>
using namespace std;
int main (void)
{
    int N, M;
    scanf("%d", &N);
    while(N--)
    {
        scanf("%d", &M);
        for (int i = 1; i <= M * 3; i++)
        {
            for (int j = 1; j <= M; j++)
                printf("HDU");
        putchar('\n');
        }
        
    }
}
